package javaPract;

public class AssignmentOperators {
	
	
	/*
	 * +, - * Divide % ++ --
	 * 
	 * 
	 * =, +=, -=, *=
	 * 
	 */
	
	 public static void main(String[] args) {
		
		int a =1;
		System.out.println(a--);//a =a-1
		System.out.println(a);
		
		int b =1;
		System.out.println(b);
		
		//b=b+1;
		b-=4;// b=b-4
		System.out.println(b);
	}
	 
	 
}
